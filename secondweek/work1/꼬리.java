public class 꼬리 {
	

	public static void main(String[] args) {
		
		for (int i = 0; i < 9; i++) {
			
			for (int j = 4; j > 2*j; j--) {
			
				System.out.print(" ");
				}
			

			for (int n = 4; n < i ; n--) {
					System.out.printf("* ");
				}
			}
			System.out.println("");
		}
	}


